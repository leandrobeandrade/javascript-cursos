let num1 = 1000000000000
console.log('num1: ', num1)

// inteiros
let num2 = 1_000_000_000_000  // melhora visualmente os números
console.log('num2: ', num2)

// decimais
let num3 = 1_575_000_000.99
console.log('num3: ', num3)

// binário
let bin = 0b1010_1010_1010
console.log('binário: ', bin)

// hexadecimal
let hex = 0xA_B_C_D_E_F
console.log('hexadecimal: ', hex)

// octal
let oct = 0o2_3_3_5
console.log('octal: ', oct)

// restrições
// 100__00   -> usar 2 underscores
// 0_5       -> começar número com 0
// 1_        -> terminar número com underscore